# Core quantitative library package.
#
# This package contains all the "brain" of the trading system:
# - data/        Market data fetching and caching
# - indicators/  Technical indicators (SMA, RSI, MACD, etc.)
# - strategies/  Trading strategy definitions and signal generation
# - backtest/    Backtesting engine that simulates historical trading
# - analytics/   Performance metrics and analysis
# - paper/       Paper (simulated) trading
# - risk/        Risk management rules
#
# MATLAB analogy: Think of this as your toolbox of .m files.
# Each sub-package is like a folder of related functions.
